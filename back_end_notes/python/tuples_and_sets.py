# Tuples, Sets, and Booleans

# Tuples are immutable sequences
# Sets are unordered collections of unique elements

# Booleans are capitalized in Python

# Tuple Examples
t = (1, 2, 3)
print(t[0])

# Can hold multiple data types

# Set Examples
x = set()
x.add(1)
x.add(2)
x.add(4)
x.add(4)
x.add(4)

print(x)

# x will still only equal {1, 2, 4}